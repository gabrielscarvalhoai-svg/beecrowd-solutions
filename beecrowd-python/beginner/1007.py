"""
Beecrowd Problem 1007 - Difference
Category: Beginner
Calculate the difference between the product of A and B and the product of C and D,
as requested in the exercise.
"""


# Input
A = int(input())
B = int(input())
C = int(input())
D = int(input())

# Processing
# DIFERENCA is the Portuguese word for DIFFERENCE
DIFERENCA = ((A * B) - (C * D))

# Output
print(f'DIFERENCA = {DIFERENCA}')
